CREATE OR REPLACE FUNCTION comp.set_master_log_id(id_database_schema numeric)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
DECLARE
VID NUMERIC;
BEGIN
	SELECT COALESCE(MAX(ID),0) INTO VID FROM TB_LOG;
	BEGIN
		INSERT INTO TB_LOG (ID_DATABASE_SCHEMA) VALUES ($1);
	EXCEPTION WHEN unique_violation THEN
		RAISE EXCEPTION USING MESSAGE = 'UNIQUE VIOLATION MAN';
	END;	
END;
$function$
