CREATE OR REPLACE FUNCTION comp.execute_statement(sql_text text)
 RETURNS boolean
 LANGUAGE plpgsql
AS $function$
BEGIN
	BEGIN
		EXECUTE $1;
		PERFORM COMP.WRITE_SQL_LOG($1);
		RETURN TRUE;
	EXCEPTION WHEN OTHERS THEN
		PERFORM COMP.WRITE_STRUCTURE_ERROR_LOG(139,$1 || CHR(13) || CHR(10) || INITCAP(SQLERRM),NULL,NULL);
		RETURN FALSE;
	END;
END;
$function$
